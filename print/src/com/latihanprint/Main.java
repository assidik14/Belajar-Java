package com.latihanprint;

public class Main {
    
    public static void main (String[] args){

        // Mencetak tanpa memasukkan baris baru
        System.out.print("Tanpa baris baru");

        // Mencetak dengan baris baru
        System.out.println("Menggunakan baris baru");

        // Mencetak dengan baris baru secara manual
        System.out.print("Beris baru secara manual \n");

        // Mencetak dengan format nomor di luar string
        System.out.printf("Angka : %d \n", 100);

    }

}
